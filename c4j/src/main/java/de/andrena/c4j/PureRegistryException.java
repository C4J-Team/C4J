package de.andrena.c4j;

public class PureRegistryException extends Exception {
	private static final long serialVersionUID = -6645697556947925967L;

	public PureRegistryException() {
		super();
	}

	public PureRegistryException(String message, Throwable cause) {
		super(message, cause);
	}

	public PureRegistryException(String message) {
		super(message);
	}

	public PureRegistryException(Throwable cause) {
		super(cause);
	}

}
